const { getInfoForDownload, getSubtitlesViaYtdlp } = require('../services/ytdl.service');
const https = require('https');
const axios = require('axios');
const { asyncHandler, AppError } = require('../middleware/errorHandler.middleware');
const { isValidYouTubeUrl, normalizeUrl } = require('../utils/urlValidator');
const { sanitizeFilename } = require('../utils/formatHelper');
const logger = require('../utils/logger');

// GET /api/subtitle/languages
const getLanguages = asyncHandler(async (req, res) => {
  const { url } = req.query;

  if (!url || !isValidYouTubeUrl(url)) {
    throw new AppError('Valid YouTube URL required', 400, 'INVALID_URL');
  }

  const info = await getInfoForDownload(url);
  let captionTracks = [];

  if (info._isYtdlp) {
    // Map yt-dlp subtitles
    const subs = info.subtitles || {};
    const autoSubs = info.automatic_captions || {};

    captionTracks = [
      ...Object.keys(subs).map(lang => ({
        languageCode: lang,
        name: { simpleText: lang },
        baseUrl: subs[lang].find(f => f.ext === 'srv3')?.url || subs[lang][0].url
      })),
      ...Object.keys(autoSubs).map(lang => ({
        languageCode: lang,
        name: { simpleText: `${lang} (auto)` },
        baseUrl: autoSubs[lang].find(f => f.ext === 'srv3')?.url || autoSubs[lang][0].url,
        kind: 'asr'
      }))
    ];
  } else {
    captionTracks = info.player_response?.captions?.playerCaptionsTracklistRenderer?.captionTracks || [];
  }

  const languages = captionTracks.map((track) => ({
    code: track.languageCode,
    name: track.name?.simpleText || track.languageCode,
    isAutoGenerated: track.kind === 'asr',
    baseUrl: track.baseUrl,
  }));

  res.json({ success: true, data: { languages } });
});

// POST /api/subtitle/download
const downloadSubtitle = asyncHandler(async (req, res) => {
  const { url, languageCode = 'en', format = 'srt' } = req.body;

  if (!isValidYouTubeUrl(url)) {
    throw new AppError('Invalid YouTube URL', 400, 'INVALID_URL');
  }

  const info = await getInfoForDownload(url);
  const details = info._isYtdlp ? info : info.videoDetails;

  let captionTracks = [];
  if (info._isYtdlp) {
    const subs = info.subtitles || {};
    const autoSubs = info.automatic_captions || {};
    captionTracks = [
      ...Object.keys(subs).map(lang => ({ languageCode: lang, baseUrl: subs[lang].find(f => f.ext === 'srv3')?.url || subs[lang][0].url })),
      ...Object.keys(autoSubs).map(lang => ({ languageCode: lang, baseUrl: autoSubs[lang].find(f => f.ext === 'srv3')?.url || autoSubs[lang][0].url }))
    ];
  } else {
    captionTracks = info.player_response?.captions?.playerCaptionsTracklistRenderer?.captionTracks || [];
  }

  const captionTrack = captionTracks.find((t) => t.languageCode === languageCode)
    || captionTracks.find((t) => t.languageCode.startsWith('en'))
    || captionTracks[0];

  if (!captionTrack) {
    throw new AppError('No subtitles/captions available for this video', 404, 'NO_SUBTITLES');
  }

  const filename = sanitizeFilename(info._isYtdlp ? info.title : details.title);

  try {
    let output = '';
    let contentType = 'text/plain';
    let ext = format;
    let xmlData = '';

    if (info._isYtdlp) {
      // Use yt-dlp to get the highly-signed subtitle URL securely, bypassing 429s
      const ytdlpSubUrl = await getSubtitlesViaYtdlp(url, languageCode, 'srv3');
      if (!ytdlpSubUrl) throw new AppError('Could not fetch subtitles via yt-dlp', 500);

      const response = await axios.get(ytdlpSubUrl, {
        headers: {
          'User-Agent': 'googlebot',
          'Referer': 'https://www.youtube.com/'
        }
      });
      xmlData = response.data;
    } else {
      let captionUrl = captionTrack.baseUrl;
      if (!captionUrl.includes('fmt=srv3')) {
        captionUrl += (captionUrl.includes('?') ? '&' : '?') + 'fmt=srv3';
      }

      const response = await axios.get(captionUrl, {
        headers: {
          'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
          'Referer': 'https://www.youtube.com/'
        }
      });
      xmlData = response.data;
    }

    if (format === 'srt') {
      output = xmlToSrt(xmlData);
      contentType = 'text/srt';
    } else if (format === 'vtt') {
      output = xmlToVtt(xmlData);
      contentType = 'text/vtt';
    } else {
      output = xmlToText(xmlData);
      contentType = 'text/plain';
    }

    res.setHeader('Content-Type', `${contentType}; charset=utf-8`);
    res.setHeader('Content-Disposition', `attachment; filename="${filename}_${languageCode}.${ext}"`);
    res.send(output);
  } catch (err) {
    logger.error('Subtitle fetch error', { error: err.message });
    if (!res.headersSent) {
      res.status(500).json({ success: false, message: 'Failed to fetch subtitles' });
    }
  }
});

// Convert XML captions to SRT format
const xmlToSrt = (xml) => {
  const lines = [];
  const regex = /<text start="([^"]+)" dur="([^"]+)"[^>]*>([^<]*)<\/text>/g;
  let match;
  let index = 1;

  while ((match = regex.exec(xml)) !== null) {
    const start = parseFloat(match[1]);
    const duration = parseFloat(match[2]);
    const end = start + duration;
    const text = decodeHtmlEntities(match[3]);

    lines.push(index);
    lines.push(`${formatSrtTime(start)} --> ${formatSrtTime(end)}`);
    lines.push(text);
    lines.push('');
    index++;
  }

  return lines.join('\n');
};

// Convert XML to VTT format
const xmlToVtt = (xml) => {
  const lines = ['WEBVTT', ''];
  const regex = /<text start="([^"]+)" dur="([^"]+)"[^>]*>([^<]*)<\/text>/g;
  let match;

  while ((match = regex.exec(xml)) !== null) {
    const start = parseFloat(match[1]);
    const duration = parseFloat(match[2]);
    const end = start + duration;
    const text = decodeHtmlEntities(match[3]);

    lines.push(`${formatVttTime(start)} --> ${formatVttTime(end)}`);
    lines.push(text);
    lines.push('');
  }

  return lines.join('\n');
};

// Convert XML to plain text
const xmlToText = (xml) => {
  const lines = [];
  const regex = /<text[^>]*>([^<]*)<\/text>/g;
  let match;

  while ((match = regex.exec(xml)) !== null) {
    const text = decodeHtmlEntities(match[1]).trim();
    if (text) lines.push(text);
  }

  return lines.join('\n');
};

const formatSrtTime = (seconds) => {
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  const ms = Math.round((seconds % 1) * 1000);
  return `${pad(h)}:${pad(m)}:${pad(s)},${pad(ms, 3)}`;
};

const formatVttTime = (seconds) => {
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  const ms = Math.round((seconds % 1) * 1000);
  return `${pad(h)}:${pad(m)}:${pad(s)}.${pad(ms, 3)}`;
};

const pad = (n, len = 2) => String(n).padStart(len, '0');

const decodeHtmlEntities = (text) => {
  return text
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\n/g, ' ')
    .trim();
};

module.exports = { getLanguages, downloadSubtitle };
